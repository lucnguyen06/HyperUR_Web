// i18n functions
function applyTranslations(lang) {
  const t = translations[lang] || translations.vi;
  window._t = t; // expose for inline JS
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) {
      el.textContent = t[key];
    }
  });
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    if (t[key] !== undefined) {
      el.innerHTML = t[key];
    }
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (t[key] !== undefined) {
      el.placeholder = t[key];
    }
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    const key = el.getAttribute('data-i18n-title');
    if (t[key] !== undefined) {
      el.title = t[key];
    }
  });
  localStorage.setItem('hyperur_lang', lang);
  document.documentElement.lang = lang;
}

function toggleLanguage() {
  const currentLang = localStorage.getItem('hyperur_lang') || 'vi';
  const newLang = currentLang === 'vi' ? 'en' : 'vi';
  applyTranslations(newLang);

  // Update lang toggle button text
  const btn = document.getElementById('langToggle');
  if (btn) {
    btn.textContent = translations[newLang]['lang.toggle'];
  }
}

function initI18n() {
  const savedLang = localStorage.getItem('hyperur_lang') || 'vi';
  applyTranslations(savedLang);

  const btn = document.getElementById('langToggle');
  if (btn) {
    btn.textContent = translations[savedLang]['lang.toggle'];
    btn.addEventListener('click', toggleLanguage);
  }
}
